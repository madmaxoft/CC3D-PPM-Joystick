/** The raw PPM data received*/
uint16_t ppmChannelValue[8] = {0};

/** The USB HID report, based on the ppmChannelValue.
Only filled in once all PPM channels are received.
Uses usbReportReady to indicate that valid data is present. */
uint16_t usbReport[5] = {0};

/** The PPM channel being currently received. */
uint8_t curChannel = 0;

/** If nonzero, indicates that the usbReport contains valid data. */
uint8_t usbReportReady = 0;





