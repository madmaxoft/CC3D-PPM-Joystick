void CopyDataToOutput()
{
	uint8_t i;
	for (i = 0; i < 4; i++)
	{
		if (ppmChannelValue[i] < 1000)
		{
			ppmChannelValue[i] = 1000;
		}
		else if (ppmChannelValue[i] > 2000)
		{
			ppmChannelValue[i] = 2000;
		}
		usbReport[i] = ppmChannelValue[i] - 1000;
	};
	usbReport[4] = 0;
	if (ppmChannelValue[4] > 1500)
	{
		usbReport[4] |= (1 << 4);
	}
	if (ppmChannelValue[5] > 1500)
	{
		usbReport[4] |= (1 << 5);
	}
	if (ppmChannelValue[6] > 1500)
	{
		usbReport[4] |= (1 << 6);
	}
	if (ppmChannelValue[7] > 1500)
	{
		usbReport[4] |= (1 << 7);
	}
	usbReportReady = 1;
}





void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef * htim)
{
	uint16_t val;
	val = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
	if ((val > 5000) && (!usbReportReady))
	{
		CopyDataToOutput();
		curChannel = 0;
	}
	else
	{
		ppmChannelValue[curChannel] = val;
		curChannel++;
		if (curChannel == 8)
		{
			curChannel = 0;
		}
	}
}




